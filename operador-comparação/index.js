/*
Operadores de comparação
> maior que
>= maior ou igual a 
< menor que
<= menor ou igual a
= atribuição
== igual a (valor)
=== igual a e do mesmo tipo (valor e tipo)
!= diferente de (valor)
!== diferente, inclusive do tipo (valor e tipo)
*/

console.log(10 > 2);
console.log(10 >= 2);
console.log(10 < 2);
console.log(10 <= 2);
console.log(10 == 2);
console.log(10 === '10');
console.log(10 != 2);
console.log(10 !== 2);